# Функция считает площадь круга.
# Формула площади круга:
# S = pi * R^2, где pi - константа 3.14, R - радиус.
# Функция возвращает значение площади.

def circle_sqr():
    r = int(input('Enter radius: '))
    pi = 3.14
    S = pi * r ** 2
    return S

