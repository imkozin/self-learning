# Написать функцию, которая в качестве аргумента принимает целое число, введенное пользователем.
# Функция возвращает количество четных цифр в числе и их сумму.
num = int(input('Enter a number: '))

def find_even(num):
    e = 0
    even_sum = 0
    for i in num:
        n = int(i)
        if n % 2 == 0:
            e += 1
            even_sum += n
    
    return e, even_sum

